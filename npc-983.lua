function sampleNPC.onTickNPC(v)
	--Don't act during time freeze
	if Defines.levelFreeze then return end
	
	local data = v.data
	
	--If despawned
	if v:mem(0x12A, FIELD_WORD) <= 0 then
		--Reset our properties, if necessary
		data.initialized = false
		return
	end

	--Initialize
	if not data.initialized then
		--Initialize necessary data.
		data.initialized = true
	end

	--Depending on the NPC, these checks must be handled differently
	if v:mem(0x12C, FIELD_WORD) > 0    --Grabbed
	or v:mem(0x136, FIELD_BOOL)        --Thrown
	or v:mem(0x138, FIELD_WORD) > 0    --Contained within
	then
		--Handling
	end

	npcutils.applyLayerMovement(v)
	
	for _,plr in ipairs(Player.get()) do
		if colliders.collide(plr, v) then

			--give points because metal cap good
			Misc.givePoints(6, vectr.v2(plr.x, plr.y), true)

			if plr.isMetal then
				SFX.Play(12)
				plr.capTimer = 0
				v:kill(9)
				return
			end

			plr.isMetal = true
			plr.metalTransformationTimer = 75
			--plr.capTimer = 0

			--[[local path = Level.filename():sub(1,-5) .. "/Super Mario 64 - Metallic Mario.ogg"

			local music = plr.sectionObj.musicID
			local isCustom = false

			if plr.sectionObj.musicID == 24 then
				music = mem(mem(0xB257B8, FIELD_DWORD)+plr.section*4, FIELD_STRING)
				isCustom = true
			end

			--if the music isn't the metal mario theme, save the music
			if not isCustom and music ~= "Super Mario 64 - Metallic Mario.ogg" then
				plr.music = music
			else
				plr.music = false
			end

			Audio.MusicChange(plr.section, path, -1)
			Audio.MusicVolume(128)]]

			v:kill(9)
		end
	end
end

